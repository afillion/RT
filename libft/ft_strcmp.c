#include "libft.h"

int		ft_strcmp(char const *s1, char const *s2)
{
	if ((*s1 == '\0') && (*s2 == '\0'))
		return (0);
	if (*s1 == *s2)
		return (ft_strcmp(s1 + 1, s2 + 1));
	return ((unsigned char)(*s1) - (unsigned char)(*s2));
}
